    <h2>Career Opportunities</h2>
    
    <h3>1. Post-Study Work Options</h3>
    <p>The USA offers several pathways for international students to gain work experience after graduation:</p>
    <ul>
        <li><strong>Optional Practical Training (OPT):</strong> Allows students to work in their field of study for up to 12 months. STEM graduates can apply for a 24-month extension.</li>
        <li><strong>Curricular Practical Training (CPT):</strong> Enables students to gain work experience while studying.</li>
    </ul>
    
    <h3>2. Employment Sectors</h3>
    <ul>
        <li><strong>Technology:</strong> Companies like Google, Microsoft, and Apple actively recruit graduates for various roles in software development, data analysis, and more.</li>
        <li><strong>Healthcare:</strong> Graduates in medicine, nursing, and public health find opportunities in hospitals, research institutions, and healthcare organizations.</li>
        <li><strong>Finance:</strong> Investment banks, consulting firms, and multinational corporations seek graduates for roles in finance, accounting, and management.</li>
        <li><strong>Education and Research:</strong> Universities and research institutions offer positions in teaching and research across diverse disciplines.</li>
    </ul>
    
