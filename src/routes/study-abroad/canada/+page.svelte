    <h2>Why Study in Canada?</h2>

    <ul>
        <li>
            <strong>Academic Excellence:</strong> Canada boasts world-renowned universities
            and colleges renowned for their quality education and research opportunities.
            Institutions like the University of Toronto, McGill University, and the
            University of British Columbia consistently rank among the best globally.
        </li>

        <li>
            <strong>Cultural Diversity:</strong> Canada embraces multiculturalism,
            offering a welcoming environment for students from diverse backgrounds.
            Nepali students can experience a blend of cultures, languages, and traditions
            while studying in Canada.
        </li>

        <li>
            <strong>Post-Graduation Opportunities:</strong> The Canadian government
            provides generous post-graduation work permits, allowing international
            students to gain valuable work experience after completing their studies.
            This serves as a pathway to permanent residency for many students.
        </li>

        <li>
            <strong>Quality of Life:</strong> With its high standard of living, safe
            cities, and breathtaking natural landscapes, Canada offers an exceptional
            quality of life. Whether it's exploring national parks or enjoying urban
            amenities, students can experience the best of both worlds.
        </li>
    </ul>

    <h2>
        Requirements for International Students (Specifically Nepali Students)
    </h2>

    <ol>
        <li>
            <strong>Academic Credentials:</strong> Nepali students need to meet academic
            requirements specified by Canadian universities or colleges. This typically
            includes a high school diploma for undergraduate programs and a bachelor's
            degree for graduate programs.
        </li>

        <li>
            <strong>Language Proficiency:</strong> Proficiency in English or French
            is essential for international students. Most institutions require standardized
            test scores, such as IELTS or TOEFL, to demonstrate language proficiency.
        </li>

        <li>
            <strong>Financial Support:</strong> Students must demonstrate sufficient
            funds to cover tuition fees, living expenses, and other costs during
            their stay in Canada. This can be shown through bank statements, sponsorships,
            or scholarships.
        </li>

        <li>
            <strong>Study Permit:</strong> Nepali students require a study permit
            to study in Canada. This permit is obtained through the Canadian consulate
            or embassy in Nepal and is typically issued alongside the acceptance
            letter from a Canadian educational institution.
        </li>

        <li>
            <strong>Health Insurance:</strong> All international students must have
            health insurance coverage for the duration of their studies in Canada.
            This ensures access to healthcare services during their stay.
        </li>
    </ol>

