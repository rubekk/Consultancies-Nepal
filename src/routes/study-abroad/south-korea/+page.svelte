    <h2>Why Study in South Korea?</h2>

    <ul>
        <li>
            <strong>Academic Excellence:</strong> South Korea boasts world-class
            universities renowned for their academic excellence and cutting-edge
            research facilities. Institutions like Seoul National University, KAIST,
            and Yonsei University consistently rank among the top in Asia and globally.
        </li>

        <li>
            <strong>Innovation Hub:</strong> As a global leader in technology and
            innovation, South Korea provides unparalleled opportunities for students
            interested in fields such as engineering, information technology, and
            biotechnology. Students benefit from access to state-of-the-art laboratories
            and collaboration with industry leaders.
        </li>

        <li>
            <strong>Cultural Experience:</strong> Immerse yourself in South Korea's
            rich cultural heritage, from ancient palaces and traditional tea ceremonies
            to modern K-pop and vibrant street markets. Studying in South Korea offers
            a unique opportunity to explore a blend of traditional values and contemporary
            lifestyle.
        </li>

        <li>
            <strong>Global Networking:</strong> South Korea's strategic location
            and growing influence in the global economy make it an ideal destination
            for students aspiring to build international networks and pursue global
            career opportunities.
        </li>
    </ul>

    <h2>Requirements for Nepali Students</h2>

    <ol>
        <li>
            <strong>Academic Qualifications:</strong> Nepali students must meet the
            academic requirements specified by South Korean universities or colleges.
            This typically includes a high school diploma for undergraduate programs
            and a bachelor's degree for postgraduate programs.
        </li>

        <li>
            <strong>Korean Language Proficiency:</strong> While many universities
            offer programs taught in English, proficiency in Korean may be required
            for certain courses or programs. Nepali students may need to demonstrate
            their Korean language skills through standardized tests such as TOPIK
            (Test of Proficiency in Korean).
        </li>

        <li>
            <strong>Student Visa:</strong> Nepali students require a student visa
            to study in South Korea. To obtain a visa, students must have an acceptance
            letter from a South Korean educational institution, proof of financial
            means to support themselves during their stay, and meet other immigration
            requirements.
        </li>

        <li>
            <strong>Health Insurance:</strong> All international students in South
            Korea are required to have health insurance. Nepali students can either
            enroll in the National Health Insurance system or obtain private health
            insurance coverage before arriving in South Korea.
        </li>
    </ol>

