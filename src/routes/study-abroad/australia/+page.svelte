    <h2>Why Study in Australia?</h2>

    <h3>1. High-Quality Education</h3>
    <p>
        Australia is known for its world-class education system and top-ranking
        universities, such as the University of Melbourne, Australian National
        University, and the University of Sydney. These institutions are
        renowned for their excellent teaching, research facilities, and academic
        resources.
    </p>

    <h3>2. Wide Range of Programs</h3>
    <p>
        Australian universities offer a diverse range of programs and courses,
        catering to various interests and career goals. Whether you are
        interested in engineering, business, health sciences, or the arts, you
        will find a program that suits your aspirations.
    </p>

    <h3>3. Multicultural Environment</h3>
    <p>
        Australia is a multicultural nation, welcoming students from all over
        the world. This creates a rich cultural environment, allowing students
        to interact with peers from different backgrounds and broaden their
        global perspectives.
    </p>

    <h3>4. Beautiful and Safe Country</h3>
    <p>
        Australia is known for its stunning landscapes, vibrant cities, and high
        quality of life. It is also one of the safest countries for
        international students, with low crime rates and a friendly, welcoming
        community.
    </p>

    <h2>
        Requirements for International Students (Specifically Nepali Students)
    </h2>

    <h3>1. Academic Requirements</h3>
    <ul>
        <li>
            <span class="bold">Diploma Programs:</span> Completion of high school (10+2) with
            a good academic standing.
        </li>
        <li>
            <span class="bold">Undergraduate Programs:</span> Completion of high school with
            a high GPA and standardized test scores (if required by the institution).
        </li>
        <li>
            <span class="bold">Graduate Programs:</span> A bachelor's degree from a recognized
            university with a good GPA and relevant standardized test scores (such
            as GMAT or GRE).
        </li>
    </ul>

    <h3>2. English Language Proficiency</h3>
    <p>
        International students must demonstrate proficiency in English through
        standardized tests like IELTS or TOEFL. The required scores vary by
        institution and program level.
    </p>

    <h3>3. Financial Proof</h3>
    <p>
        Students must show proof of sufficient funds to cover tuition fees,
        living expenses, and other costs. This includes bank statements,
        scholarship letters, or sponsorship letters.
    </p>

    <h3>4. Student Visa</h3>
    <p>
        Nepali students need a student visa (subclass 500) to study in
        Australia. This requires a Confirmation of Enrolment (CoE) from an
        Australian educational institution and proof of financial capacity.
    </p>

    <h3>5. Overseas Student Health Cover (OSHC)</h3>
    <p>
        International students are required to have health insurance through
        OSHC for the duration of their stay in Australia.
    </p>

